import { readFileSync, writeFileSync } from 'node:fs'
const manifest = JSON.parse(readFileSync('public/lottie/manifest.json', 'utf8'))

const camel = s => s.replace(/-(.)/g, (_, c) => c.toUpperCase())
// unique exercises in declaration order
const slugs = [...new Set(manifest.map(m => m.slug))]
const meta = {}
for (const m of manifest) meta[m.slug] = { name: m.name, level: m.level, focus: m.focus }

const genders = ['male', 'female']
const varName = (slug, g) => camel(slug) + g[0].toUpperCase() + g.slice(1)

const imports = []
for (const slug of slugs)
  for (const g of genders)
    imports.push(`import ${varName(slug, g)} from '@/public/lottie/${g}/${slug}.json'`)

const rows = []
for (const slug of slugs) {
  for (const g of genders) {
    const m = meta[slug]
    rows.push(`  {
    id: '${slug}-${g}',
    slug: '${slug}',
    name: ${JSON.stringify(m.name)},
    level: '${m.level}',
    focus: ${JSON.stringify(m.focus)},
    gender: '${g}',
    data: ${varName(slug, g)} as unknown as object,
    file: '/lottie/${g}/${slug}.json',
    levelFile: '/lottie/${m.level}/${slug}-${g}.json',
    creator: '75DaysLab',
    source: 'Original asset — generated in-house (see scripts/gen-lottie.mjs)',
    license: 'Project-owned · free for unlimited commercial use',
    styleScore: 100,
  },`)
  }
}

const out = `// AUTO-GENERATED by scripts/gen-registry.mjs — do not edit by hand.
// Consistent flat-design exercise animations, one shared rig + palette.
// Every asset is original (owned by 75DaysLab): no third-party license, free commercial use.
// Style consistency is exact across the set (single rig) → styleScore 100.
import type { FC } from 'react'

${imports.join('\n')}

export type ExerciseLevel = 'beginner' | 'intermediate' | 'advanced'
export type Gender = 'male' | 'female'
export type ExerciseFocus = 'full' | 'core' | 'upper' | 'lower' | 'cardio' | 'yoga'

export interface ExerciseLottie {
  /** unique key: \`\${slug}-\${gender}\` */
  id: string
  /** exercise slug, shared across genders */
  slug: string
  name: string
  level: ExerciseLevel
  focus: ExerciseFocus[]
  gender: Gender
  /** parsed Lottie JSON — pass straight to lottie-react's animationData */
  data: object
  /** public URL under public/lottie/<gender>/ (for fetch-based players) */
  file: string
  /** public URL under public/lottie/<level>/ (level-organized copy) */
  levelFile: string
  creator: string
  source: string
  license: string
  /** 0–100; all share one rig so consistency is exact */
  styleScore: number
}

export const EXERCISE_LOTTIES: ExerciseLottie[] = [
${rows.join('\n')}
]

export const byLevel = (level: ExerciseLevel): ExerciseLottie[] =>
  EXERCISE_LOTTIES.filter(e => e.level === level)

export const byGender = (gender: Gender): ExerciseLottie[] =>
  EXERCISE_LOTTIES.filter(e => e.gender === gender)

export const byFocus = (focus: ExerciseFocus): ExerciseLottie[] =>
  EXERCISE_LOTTIES.filter(e => e.focus.includes(focus))

export const bySlug = (slug: string): ExerciseLottie[] =>
  EXERCISE_LOTTIES.filter(e => e.slug === slug)

export const findExercise = (slug: string, gender: Gender): ExerciseLottie | undefined =>
  EXERCISE_LOTTIES.find(e => e.slug === slug && e.gender === gender)

/**
 * Ready-to-use lottie-react example:
 *
 *   import Lottie from 'lottie-react'
 *   import { findExercise } from '@/lib/fitness/exerciseLottieRegistry'
 *
 *   const squat = findExercise('squat', 'male')!
 *   <Lottie animationData={squat.data} loop autoplay style={{ width: 240 }} />
 */
export type ExerciseLottieComponent = FC<{ slug: string; gender?: Gender }>
`
writeFileSync('lib/fitness/exerciseLottieRegistry.ts', out)
console.log('wrote lib/fitness/exerciseLottieRegistry.ts —', slugs.length, 'exercises x', genders.length, 'genders =', slugs.length * genders.length, 'entries')
